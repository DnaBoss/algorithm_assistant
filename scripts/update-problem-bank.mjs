import { writeFileSync, readFileSync } from 'node:fs'
import ts from 'typescript'

async function getJson(url) {
  const res = await fetch(url, { headers: { 'user-agent': 'Mozilla/5.0' } })
  if (!res.ok) throw new Error(`${url} ${res.status}`)
  return res.json()
}

function importTs(path) {
  const source = readFileSync(new URL(path, import.meta.url), 'utf8')
  const js = ts.transpileModule(source, { compilerOptions: { module: ts.ModuleKind.ES2022, target: ts.ScriptTarget.ES2022 } }).outputText
  return import(`data:text/javascript;base64,${Buffer.from(js).toString('base64')}`)
}

const [leetcode, ratings, tutorialMod, searchMod] = await Promise.all([
  getJson('https://leetcode.com/api/problems/all/'),
  getJson('https://zerotrac.github.io/leetcode_problem_rating/data.json'),
  importTs('../src/tutorialData.ts'),
  importTs('../src/search.ts')
])

const tutorialByNumber = new Map(tutorialMod.tutorials.map(t => [String(searchMod.problemNumberFor(t.id)), t]))
const ratingBySlug = new Map(ratings.map((r, i) => [r.TitleSlug, { ...r, ratingRank: i + 1 }]))
const difficulty = { 1: 'Easy', 2: 'Medium', 3: 'Hard' }
const commonExtra = new Set([
  4, 31, 32, 34, 36, 42, 45, 51, 57, 84, 85, 94, 96, 97, 99, 103, 107, 108, 110, 111, 113, 114, 116, 118, 119, 127, 129, 130, 131, 134, 138, 140, 146, 148, 149, 152, 153, 154, 155, 160, 162, 166, 172, 188, 199, 207, 208, 210, 212, 215, 221, 227, 234, 239, 253, 269, 279, 295, 297, 300, 301, 309, 312, 329, 332, 341, 347, 394, 416, 438, 494, 516, 518, 543, 560, 621, 739, 746, 763, 787, 875, 994, 1143
])

const bank = leetcode.stat_status_pairs
  .map(item => {
    const stat = item.stat
    const idNum = Number.parseInt(stat.frontend_question_id, 10)
    const rating = ratingBySlug.get(stat.question__title_slug)
    const tutorial = tutorialByNumber.get(String(idNum))
    const reasons = []
    if (tutorial?.tags.includes('Blind 75') || tutorial?.tags.includes('LeetCode 75')) reasons.push('Blind/LeetCode 75')
    if (tutorial?.tags.includes('Top 150') || tutorial?.tags.includes('LeetCode Top 150')) reasons.push('Top 150')
    if (commonExtra.has(idNum)) reasons.push('常見面試/網路高頻')
    if (rating && rating.Rating >= 1200 && rating.Rating <= 2200) reasons.push('競賽 rating 適合練習')
    if (rating?.ProblemIndex === 'Q4') reasons.push('週賽高價值 Q4')
    return {
      id: String(stat.frontend_question_id),
      title: stat.question__title,
      slug: stat.question__title_slug,
      difficulty: difficulty[item.difficulty?.level] ?? 'Unknown',
      paidOnly: Boolean(item.paid_only),
      acceptance: stat.total_submitted ? Number(((stat.total_acs / stat.total_submitted) * 100).toFixed(1)) : null,
      rating: rating ? Math.round(rating.Rating) : null,
      ratingRaw: rating ? Number(rating.Rating.toFixed(2)) : null,
      ratingRank: rating?.ratingRank ?? null,
      contest: rating?.ContestID_en ?? null,
      contestSlug: rating?.ContestSlug ?? null,
      problemIndex: rating?.ProblemIndex ?? null,
      hasTutorial: Boolean(tutorial),
      tags: tutorial?.tags ?? [],
      highValue: reasons.length > 0,
      reasons
    }
  })
  .sort((a, b) => Number(a.id) - Number(b.id))

const contestCount = bank.filter(p => p.contest).length
const highValueCount = bank.filter(p => p.highValue).length
const source = `// Generated by scripts/update-problem-bank.mjs\nexport type ProblemBankItem = {\n  id: string\n  title: string\n  slug: string\n  difficulty: string\n  paidOnly: boolean\n  acceptance: number | null\n  rating: number | null\n  ratingRaw: number | null\n  ratingRank: number | null\n  contest: string | null\n  contestSlug: string | null\n  problemIndex: string | null\n  hasTutorial: boolean\n  tags: string[]\n  highValue: boolean\n  reasons: string[]\n}\n\nexport const problemBankMeta = ${JSON.stringify({ total: bank.length, ratedContestProblems: contestCount, highValueProblems: highValueCount, generatedAt: new Date().toISOString(), sources: ['leetcode.com/api/problems/all', 'zerotrac.github.io/leetcode_problem_rating/data.json'] }, null, 2)} as const\n\nconst problemBankJson = ${JSON.stringify(JSON.stringify(bank))}\nexport const problemBank: ProblemBankItem[] = JSON.parse(problemBankJson)\n`
writeFileSync(new URL('../src/problemBank.ts', import.meta.url), source)
console.log(`Wrote ${bank.length} problems, ${contestCount} rated contest problems, ${highValueCount} high-value problems to src/problemBank.ts`)
